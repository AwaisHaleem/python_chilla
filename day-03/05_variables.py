# variables: objects containing specific values
name = "Awais"  # string variable
age = 2  # int variable

# types of variables
print(type(name))  # string class/type
print(type(age))  # int class


# Rules to assign variables
# 1. don't start with numbers
# 2. use alphabets, numbers and underscore
# 3. don't use spaces
# 4. don't use keywords of python
# 5. short and descriptive variable
# 6. case senstivity

fruit_basket = "mango", 'orange'
print(fruit_basket)
print(type(fruit_basket))
